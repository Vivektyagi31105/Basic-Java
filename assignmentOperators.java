import java.util.*;

public class assignmentOperators {

    public static void main(String[] args) {
        int i=10;

        int j=0;
        j=j+1;
        j+=1;

        System.out.println(j);
        
        System.out.println(i+=5);
        System.out.println(i-=3);
        System.out.println(j*=2);
        System.out.println(i/=2);
    }
}
